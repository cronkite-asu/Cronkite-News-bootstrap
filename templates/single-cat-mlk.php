		  <h3>‘Voices of the silenced’</h3>
		  <p>Not only are riots the language of the unheard, they are the voice of the silenced. Because just think, if all of those rioters out there had law degrees, if all of them had politician jobs, if all of them owned a billion-dollar business, do you think they would be looting or do you think they'd be strategizing?</p>

		  <h3>Riots are like war</h3>
		  <p>I am and have always been, even as a Black bisexual woman, I have always lived in the gray area. And that's what I'm going to call this. I feel like the common denominator of both of those things is strategy.</p>
		  <p>If violence and if rioting were things that didn't work, why is it that in history everything starts with a war?</p>
		  <p>If our leaders don't want us to lead with war, then they have to show by example. Let's not occupy Iraq, let's instead be diplomatic and go speak to their leaders, let's speak to their people. Let's not just go and loot the whole continent of Africa. Instead, let's ask that they share their gold, ask that they share their people, let's ask that they share and let us view their art.</p>
		  <p>Let's not, as a country, go to war to prove a point. But until somebody can lead by example, that's what has been taught. That works, you know?</p>

		  <h3>‘To protect and to serve,’ not kill</h3>
		  <p>The police are not made to hunt us. They're not made to prosecute and convict. They're made to protect and serve. So when I call you and tell you, ‘Hey, my husband's in prison, I can't get the kids to school,’ It's your job to pull up to my place and take the kids to school. That's protect and serve. That's part of serving.</p>
