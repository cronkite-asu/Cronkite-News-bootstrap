		  <h3>The words of Martin Luther King Jr.</h3>
		  <p class="quote"><a href="https://kinginstitute.stanford.edu/king-papers/documents/letter-birmingham-jail" target="_blank">Injustice anywhere</a> is a threat to justice everywhere.</p>

		  <h3>Why MLK’s words resonate</h3>
		  <p>We're robbed as soon as we're born. We're robbed of opportunities coming out of the womb.</p>
		  <p>We deserve to have equal opportunities. We deserve to be able to go into any club if we want to in Scottsdale and not feel discriminated against. Those are rights, human rights, human decency. And there's a bit of a gap here in Arizona.</p>
		</div>
		<div id="percy-rest">
		  <p>This place is very rooted in racism. And the only way that we can start to see that change and get rid of that vicious cycle is to try to just uproot it from the system, get people to wake up, get people to talk about it, have those difficult, difficult conversations. And that's part of what a protest will do.</p>

		  <h3>Frustration leads to riots</h3>
		  <p>You can only become so frustrated, to the point where you can't take it anymore. Where enough is enough. And people like to paint MLK as a very calm person. And he was a very peaceful type of man. But I think at that point he was probably very frustrated. He never saw the change that he really wanted to see –  he was murdered before he got to see it. He probably understood, to a certain extent, why people did choose to riot instead of protesting in the streets.</p>
		  <p>I was out there in downtown Phoenix Thursday night, Friday night, Saturday night and Sunday night, the first four nights, where the city of Phoenix Police Department – they tear-gassed us. They shot rubber bullets directly at protesters. Those types of things, they cause a certain frustration. Now personally, I don't believe in rioting and I don't support rioting. But I think that at a certain point, people who have been through that, if that's what they feel is their way of expressing it, I'm not here to tell them no.</p>
		  <p>People are always going to be against destruction. People are always going to be against tearing things that they've built and established down. You’re always going to have people against that. But who can be against peace? Who can be against somebody being peaceful? How can you be against that?</p>
		  <p>Personally, I agree with MLK. I think I agree with the nonviolence thing. But I would never put down the people who choose to do the opposite. Because we all want the same thing.</p>

		  <h3>Keeping the Black Lives Matter movement alive</h3>
		  <p>It is still a fire. So we're going to most definitely try to keep it alight and keep it ignited and keep going with it, whatever that looks like.</p>
