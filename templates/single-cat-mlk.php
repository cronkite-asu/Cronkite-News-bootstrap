		  <h3>The words of Martin Luther King Jr.</h3>
		  <p class="quote">I submit that an individual who breaks a law that <a href="https://kinginstitute.stanford.edu/" target="_blank">conscience tells him is unjust</a>, and who willingly accepts the penalty of imprisonment in order to arouse the conscience of the community over its injustice, is in reality expressing the highest respect for law.</p>

		  <h3>Why MLK’s words resonate</h3>
		  <p>I think a lot of people equate peaceful with law-abiding.  I think that there's a difference. You can nonviolently protest or defend yourself or your protest, but nonviolently protesting doesn't always mean that you're always within the bounds of the law.</p>
		  <p>Sometimes protest requires the infraction of laws, but it's in a way that's in line with your conscience. It's not just to be a law breaker. It's to break what you perceive to be an unjust law. And the idea being that unjust laws can't be consciously followed because they promote injustice. And I think a lot of people don't like to think about that when they think about what the word peaceful means.</p>
		</div>

		<div id="jose-audio">
		  <h3>What 'rioters' are trying to tell people</h3>
		  <p>It's not an act of violence because it's not intended to hurt anyone. So many more people are outraged about the destruction of property rather than the annihilation of bodies.</p>
		  <p>Instead of using words like vandalism or arson or property damage, you use the word riot to make it seem to make it seem unjust and violent. But nobody refers to democratic revolutions as rioting. They call it a political movement. They call it patriotic. They call it revolutionary.</p>
		  <p>It's a language, and we're trying to communicate because we aren’t heard when we're speaking quietly. We weren't heard going to City Council meetings. We weren't heard writing to our senators, putting letters in the mailbox. The idea that people of color and minorities as a unified group have suffered deprivations of their rights and their dignity, over 400 years, is being distilled into just windows being broken, is completely missing the entire point.</p>

		  <h3>Protest is patriotic</h3>
		  <p>When celebrities and noteworthy people use their position to do whatever it happens to be that’s a silent or calm or civil way of communicating their problem, they're labeled as being unpatriotic … as being ungrateful … as being irreverent or hateful.</p>
		  <p>We abide by the same laws. We pay the same taxes. We go through the same processes that everybody else goes through. We utilize the same constitutional rights to protest and to make our opinions heard. But we're still labeled as being ungrateful. I believe that there is nobility in nonviolent forms of expression. But the idea is that even as we do that, we're seen as unpatriotic.</p>
		  <p>When your noble form of expression is constantly being silenced, it's constantly being ignored, it's constantly being disregarded and trampled upon, and does not seem to be on the same level as other people's expressions or grievances – what's supposed to happen? At that point, what else is there to do?</p>

		  <h3>Defund police to redirect money</h3>
		  <p>Anybody who wants to just stop at the word defund itself and treat that word as if it has no other implications, is just showing an unwillingness to talk about the fact that art programs are always being defunded, education programs. ... It's just the reality of the economy and the budget that certain programs that no longer are useful in the way that they were supposed to be, or that might be redundant, or that might be a product of a different time are deconstructed, so that money can be put to better use.</p>
		  <p>I believe instead of criminalizing and punishing homelessness, which feeds back into the role of police, that if a portion of that money was taken from defunding and reallocated, it should go towards directives that help to decriminalize things like homelessness. Or decriminalize the public outbursts that mental illness can create, and go towards programs that, again, heal those problems, instead of treating them as crimes.</p>

		  <h3>Take risks to honor MLK</h3>
		  <p>With these public heroes and these idols that we have, sometimes we elevate them to a position that we believe makes them perfect, or incorrigible, that they're unable to be tainted, when it's more respectful and powerful to see them as human beings that have complexities.</p>
		  <p>The best way that you can honor somebody like Martin Luther King is by not just sharing a quote, not just sharing a sentence, but using it as a way to start conversations, uncomfortable conversations, conversations that might turn into arguments. And those arguments might turn into ones that you don't win. You might have those arguments, and you might lose somebody who you consider to be a friend. You might alienate yourself from a family member. There's all sorts of outcomes from those conversations, but you have to be willing to have them.</p>
