		  <h3>The words of Martin Luther King Jr.</h3>
		  <p class="quote"><a href="https://www.google.com/search?q=i+have+a+dream+speech&oq=I+have+a+dream&aqs=chrome.0.0j46j69i57j69i59j0l4.3231j0j4&sourceid=chrome&ie=UTF-8" target="_blank">I have a dream</a> that one day this nation will rise up and live out the true meaning of its creed.  We hold these truths to be self-evident, that all men are created equal.</p>

		  <h3>What she would have changed</h3>
		  <p>I would have edited it to “I have a plan.”</p>
		  <p>Of course, he had to have a dream back then. Because to get a million people on a plan with just a microphone would have been really hard and really public. You know what I mean? So his plan would have been refuted easily, I guess. So I feel like nowadays, if he had the technology we had, he would have been like, ‘Look, I got a plan. And here it go. Here go the schematics.’</p>
		</div>

